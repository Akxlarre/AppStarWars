    public void cargarTabla(){
        String[] encabezado={"NOMBRE","ID", "EDAD","BANDO","PELICULAS"};
        DefaultTableModel modelo=new DefaultTableModel(encabezado, 0);
        String[] datos=new String[5];
        for (Personaje pj : Principal2.db.mostrarPj()) {
            datos[0]=pj.getNombre();
            datos[1]=String.valueOf(pj.getId());
            datos[2]=String.valueOf(pj.getEdad());
            datos[3]=pj.getBando();
        // me falta agregar las peliculas 
            
            modelo.addRow(datos);
        }
        tablaListar.setModel(modelo);
    }
